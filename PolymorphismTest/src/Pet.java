class Pet {
	    void petSound() {
		    System.out.println("The animal makes a loud snickering sound");
		  }
		}

		class Dog extends Pet {
		   public void petSound() {
		    System.out.println("The Dog says: woff woff ");
		  }
		}
		
		class Lion extends Pet {
		   public void petSound() {
		    System.out.println("The Lion says: Roar");
		  }
		}

