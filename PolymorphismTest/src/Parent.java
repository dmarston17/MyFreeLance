
 class Parent {

	// Java program for Method overriding
	 
	   
	     void Print()
	     {
	         System.out.println("parent class");
	     }
	 }
	   
	 class subclass1 extends Parent {
	   
	     void Print()
	     {
	         System.out.println("subclass1");
	     }
	 }
	   
	 class subclass2 extends Parent {
	   
	     void BuildMyApp()
	     {
	         System.out.println("subclass2");
	     }
	 }
	   
